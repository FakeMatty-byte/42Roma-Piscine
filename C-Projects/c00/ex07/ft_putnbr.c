#include <unistd.h>

void	ft_putnbr(int nb)
{
	long	n;
	long	nb2;

	nb2 = nb;
	if (nb2 < 0)
	{
		nb2 *= -1;
		write(1, "-", 1);
	}
	if (nb2 < 10)
	{
		nb2 += 48;
		write(1, &nb2, 1);
	}
	else
	{
		n = nb2 % 10 + 48;
		ft_putnbr(nb2 / 10);
		write(1, &n, 1);
	}
}
